N = int(input())
a = 0
arr = [] # 숫자를 담을 리스트

def Back():
    # 연달아 숫자가 나오는 시작 위치를 잡는다.
    i = 0 # 검사 시작 위치 인덱스 i
    while i < N:
        # 범위 쳌
        # 연속하여 해당 숫자만큼 나올 수 없다면... NO아름다움
        # 만약 현재 숫자가 k인데, 남은 자릿수가 k개보다 적다면

        if i + arr[i] > N:
            return False
        # 연속하여 해당 숫자만큼 같은 숫자가 있는지 봄
        # 하나라도 다른 숫자가 잇다면 no아름다움
        # 연속성 체크
        for j in range(i,i+arr[i]):
            if arr[j] != arr[i]:
                return False

        # 다음 묶음으로 이동
        i += arr[i]

    return True

def TRACKING(cnt):
    global a

    # 종료조건
    if cnt == N:
        if Back(): # 아름다운 수인지 검사
            a += 1
        return

    # 재귀 호출 : 1부터 4까지의 숫자를 하나씩 넣어봄
    for i in range(1,5): # 1부터 4까지니까
        arr.append(i) # 숫자 선택 (자리에 넣기)
        TRACKING(cnt+1)  # 다음 자리로 이동
        arr.pop()  # 되돌리기 == 가지치기
        


TRACKING(0)
print(a)




