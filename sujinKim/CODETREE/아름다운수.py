### 즉, 각 자리에 대해 1-4사이의 숫자 중 하나를 선택하여
### 총 N번 선택하는 재귀를 작성하여 가능한 모든 숫자들을 만들어낸다
### 그 중 정확히 해당 숫자만큼 연달아 같은 숫자가 나오는 숫자의 수를 세면 된다


### 재귀를 공부해보자 ###
# 재귀의 틀 # ex. 1부터 N까지 자연수의 합을 구하는 코드를 작성한다.
